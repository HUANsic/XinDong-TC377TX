#include <stdio.h>

extern IfxCpu_syncEvent g_cpuSyncEvent;

float range = 0.33, output = 0, increment = 0.1, direction = 1;
uint8 state = 0;

char startMsg[] = "Servo Example";
char angleMsg[30];

void core2_main(void) {
	IfxCpu_enableInterrupts();
	/* !!WATCHDOG2 IS DISABLED HERE!!
	 * Enable the watchdog and service it periodically if it is required
	 */
	IfxScuWdt_disableCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword());

	/* Wait for CPU sync event */
	IfxCpu_emitEvent(&g_cpuSyncEvent);
	IfxCpu_waitEvent(&g_cpuSyncEvent, 1);

	// wait for signal to begin initialization
	while (Intercore_InitAllowed() == 0)
		;

	// initialize any module needed
	Serial_Init();
	Bluetooth_Init();
	ADC_Init();
	Ultrasonic_Init();
	OLED_Init();
	MPU6050_Init();
	Encoder_Init();
	Servo_Init();
	Motor_Init();
	PID_Init(0.1, 0.0, 0.0);

	// wait for other cores to finish initialization
	Intercore_CPU2_Ready();
	while (Intercore_ReadyToGo() == 0)
		;

	// set servo software center
	Servo_SetCenter(0);
	// set demo step size
	increment = range / 10;
	// prepare display
	OLED_ShowString(0, 0, startMsg, OLED_8X16);
	OLED_Update();
	Time_Delay(1000);

	// main loop
	while (1) {
		// some code to indicate that the core is not dead
		IO_LED_Toggle(3);
		Time_Delay_us(300000);

		// update state
		state = (IO_DIP_Read(2) ? 2 : 0) + (IO_DIP_Read(1) ? 1 : 0);
		// depending on state, perform corresponding task
		switch (state) {
		case 0: // center mode
			output = 0;
			Servo_Set(output);
			break;
		case 1:// sweep mode
			output += increment * direction;
			if (output > range - increment || output < -range + increment)
				direction *= -1;
			Servo_Set(output);
			break;
		case 2:// follow mode
		case 3:// follow mode
			// calculate output position from potentiometer
			output = (ADC_GetVoltage(0) / 3.3 * 2 - 1) * range;
			// set output
			Servo_Set(output);
			break;
		default:
			break;
		}

		// print output onto display
		sprintf(angleMsg, "Output=%0.3f ", output);
		OLED_ShowString(16, 32, angleMsg, OLED_8X16);
		OLED_Update();
	}
}

